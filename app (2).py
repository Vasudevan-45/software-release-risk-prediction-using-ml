def style_fig(fig):
    """Apply a dark, transparent theme to a Plotly figure so it blends with the app background."""
    fig.update_layout(
        template="plotly_dark",
        paper_bgcolor="rgba(0,0,0,0)",
        plot_bgcolor="rgba(0,0,0,0)",
        font_color="#eef2f5",
        legend=dict(bgcolor="rgba(0,0,0,0)"),
        margin=dict(t=30, b=20, l=20, r=20),
    )
    return fig

# -----------------------------
# Compatibility shim
# -----------------------------
# The model was pickled with numpy 2.x, which renamed numpy.core to
# numpy._core internally. Older numpy versions (<2.0) don't have this
# module, so joblib.load() fails with "ModuleNotFoundError: No module
# named 'numpy._core'". Only patch this in when it's actually missing —
# on numpy>=2.0 the real numpy._core already exists and force-aliasing
# it to numpy.core breaks other imports (e.g. numpy.strings).
if "numpy._core" not in sys.modules:
    try:
        import numpy._core  # noqa: F401  (already present on numpy>=2.0)
    except ImportError:
        import numpy.core as core
        sys.modules["numpy._core"] = core

