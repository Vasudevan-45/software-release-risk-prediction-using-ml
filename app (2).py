# -----------------------------
# Load Model
# -----------------------------
model = joblib.load("release_risk_model.pkl")
label_encoder = joblib.load("label_encoder.pkl")

# -----------------------------
# Title
# -----------------------------
st.title("🚀 Intelligent Software Release Risk Prediction System")

st.write("""
Predict the **risk level** of a software release using Machine Learning, and explore
the underlying dataset through SQL-driven analytics.
""")

st.markdown("---")

tab_predict, tab_insights = st.tabs(["🔮 Risk Prediction", "📊 SQL Insights Dashboard"])

with tab_predict:

    # -----------------------------
    # Sidebar Inputs
    # -----------------------------
    st.sidebar.header("Enter Software Release Details")

    commits = st.sidebar.number_input("Commits", 0, 1000, 150)

    bugs = st.sidebar.number_input("Bugs Reported", 0, 100, 10)

    critical = st.sidebar.number_input("Critical Bugs", 0, 20, 2)

    coverage = st.sidebar.slider("Test Coverage (%)", 0.0, 100.0, 85.0)

    failed = st.sidebar.number_input("Failed Test Cases", 0, 100, 5)

    churn = st.sidebar.number_input("Code Churn", 0, 20000, 2500)

    complexity = st.sidebar.number_input("Cyclomatic Complexity", 1, 100, 15)

    files = st.sidebar.number_input("Files Changed", 1, 300, 30)

    experience = st.sidebar.slider("Developer Experience (Years)", 0.0, 20.0, 5.0)

    security = st.sidebar.number_input("Security Vulnerabilities", 0, 20, 1)

    build = st.sidebar.number_input("Build Time (Minutes)", 1, 120, 20)

    deployment = st.sidebar.number_input("Deployment Frequency", 1, 30, 6)

    # -----------------------------
    # Prediction Data
    # -----------------------------
    input_data = pd.DataFrame({
        "Commits":[commits],
        "Bugs_Reported":[bugs],
        "Critical_Bugs":[critical],
        "Test_Coverage":[coverage],
        "Failed_Test_Cases":[failed],
        "Code_Churn":[churn],
        "Cyclomatic_Complexity":[complexity],
        "Files_Changed":[files],
        "Developer_Experience":[experience],
        "Security_Vulnerabilities":[security],
        "Build_Time_Minutes":[build],
        "Deployment_Frequency":[deployment]
    })

    # -----------------------------
    # Display Input
    # -----------------------------
    st.subheader("Input Data")

    st.dataframe(input_data)

    # -----------------------------
    # Prediction
    # -----------------------------
    if st.button("Predict Risk"):

        prediction = model.predict(input_data)

        prediction_label = label_encoder.inverse_transform(prediction)[0]

        probability = model.predict_proba(input_data)

        confidence = probability.max() * 100

        st.markdown("---")

        st.header("Prediction Result")

        if prediction_label == "Low":
            st.success(f"✅ Release Risk : {prediction_label}")

        elif prediction_label == "Medium":
            st.warning(f"⚠️ Release Risk : {prediction_label}")

        else:
            st.error(f"🚨 Release Risk : {prediction_label}")

        st.write(f"### Confidence : {confidence:.2f}%")

        st.markdown("---")

        st.subheader("Probability of Each Risk Level")

        prob_df = pd.DataFrame({
            "Risk Level": label_encoder.classes_,
            "Probability (%)": probability[0] * 100
        })

        st.dataframe(prob_df)

        st.bar_chart(
            prob_df.set_index("Risk Level")
        )

        st.markdown("---")

        st.subheader("Recommendation")

        if prediction_label == "Low":

            st.success("""
            ✔ Ready for Deployment

            • Release is stable

            • Testing coverage is good

            • Proceed with deployment
            """)

        elif prediction_label == "Medium":

            st.warning("""
            Review before Deployment

            • Verify failed test cases

            • Improve test coverage

            • Review critical bugs
            """)

        else:

            st.error("""
            High Risk Release

            • Delay deployment

            • Fix critical bugs

            • Resolve security vulnerabilities

            • Increase testing coverage

            • Rebuild before release
            """)

with tab_insights:

    st.header("📊 SQL Insights Dashboard")
    st.caption(
        "Every chart below is powered by a SQL query against a SQLite database "
        "built from `software_release_risk_dataset.csv`. See `analysis_queries.sql` "
        "for the raw queries."
    )

    # -----------------------------
    # Build / connect to SQLite DB
    # -----------------------------
    DB_PATH = "release_risk.db"
    CSV_PATH = "software_release_risk_dataset.csv"

    if not os.path.exists(DB_PATH) and os.path.exists(CSV_PATH):
        _df = pd.read_csv(CSV_PATH)
        _conn = sqlite3.connect(DB_PATH)
        _df.to_sql("releases", _conn, if_exists="replace", index=False)
        _conn.close()

    conn = sqlite3.connect(DB_PATH)

    RISK_COLORS = {"Low": "#2ecc71", "Medium": "#f39c12", "High": "#e74c3c"}

    # ---- KPI row ----
    kpi_sql = """
        SELECT
            COUNT(*) AS total,
            SUM(CASE WHEN Risk_Level='High' THEN 1 ELSE 0 END) AS high_count,
            ROUND(AVG(Test_Coverage), 1) AS avg_coverage,
            SUM(Security_Vulnerabilities) AS total_vulns
        FROM releases
    """
    kpi = pd.read_sql_query(kpi_sql, conn).iloc[0]

    k1, k2, k3, k4 = st.columns(4)
    k1.metric("Total Releases", int(kpi["total"]))
    k2.metric("High Risk Releases", int(kpi["high_count"]))
    k3.metric("Avg Test Coverage", f"{kpi['avg_coverage']}%")
    k4.metric("Total Security Vulns", int(kpi["total_vulns"]))

    st.markdown("---")

    col1, col2 = st.columns(2)

    # ---- 1. Risk level distribution ----
    with col1:
        st.subheader("Risk Level Distribution")
        dist_sql = """
            SELECT Risk_Level, COUNT(*) AS Release_Count
            FROM releases
            GROUP BY Risk_Level
        """
        dist_df = pd.read_sql_query(dist_sql, conn)
        fig1 = px.pie(
            dist_df, names="Risk_Level", values="Release_Count",
            color="Risk_Level", color_discrete_map=RISK_COLORS, hole=0.45
        )
        st.plotly_chart(style_fig(fig1), use_container_width=True)

    # ---- 2. Avg metrics by risk level ----
    with col2:
        st.subheader("Avg Critical Bugs & Security Vulns by Risk")
        avg_sql = """
            SELECT Risk_Level,
                   AVG(Critical_Bugs) AS Avg_Critical_Bugs,
                   AVG(Security_Vulnerabilities) AS Avg_Security_Vulns
            FROM releases
            GROUP BY Risk_Level
        """
        avg_df = pd.read_sql_query(avg_sql, conn)
        avg_df = avg_df.melt(id_vars="Risk_Level", var_name="Metric", value_name="Average")
        fig2 = px.bar(
            avg_df, x="Risk_Level", y="Average", color="Metric", barmode="group",
            category_orders={"Risk_Level": ["Low", "Medium", "High"]}
        )
        st.plotly_chart(style_fig(fig2), use_container_width=True)

    col3, col4 = st.columns(2)

    # ---- 3. Test coverage buckets vs risk ----
    with col3:
        st.subheader("Test Coverage Bucket vs Risk Level")
        bucket_sql = """
            SELECT
                CASE
                    WHEN Test_Coverage < 50 THEN '0-50%'
                    WHEN Test_Coverage < 70 THEN '50-70%'
                    WHEN Test_Coverage < 85 THEN '70-85%'
                    ELSE '85-100%'
                END AS Coverage_Bucket,
                Risk_Level,
                COUNT(*) AS Release_Count
            FROM releases
            GROUP BY Coverage_Bucket, Risk_Level
        """
        bucket_df = pd.read_sql_query(bucket_sql, conn)
        fig3 = px.bar(
            bucket_df, x="Coverage_Bucket", y="Release_Count", color="Risk_Level",
            color_discrete_map=RISK_COLORS, barmode="stack",
            category_orders={"Coverage_Bucket": ["0-50%", "50-70%", "70-85%", "85-100%"]}
        )
        st.plotly_chart(style_fig(fig3), use_container_width=True)

    # ---- 4. Code churn vs test coverage scatter, colored by risk ----
    with col4:
        st.subheader("Code Churn vs Test Coverage")
        scatter_sql = """
            SELECT Code_Churn, Test_Coverage, Risk_Level, Critical_Bugs
            FROM releases
        """
        scatter_df = pd.read_sql_query(scatter_sql, conn)
        fig4 = px.scatter(
            scatter_df, x="Code_Churn", y="Test_Coverage", color="Risk_Level",
            size="Critical_Bugs", color_discrete_map=RISK_COLORS, opacity=0.6
        )
        st.plotly_chart(style_fig(fig4), use_container_width=True)

    st.markdown("---")

    # ---- 5. Developer experience vs critical bugs ----
    st.subheader("Developer Experience vs Avg Critical Bugs / Security Vulns")
    exp_sql = """
        SELECT
            CASE
                WHEN Developer_Experience < 2 THEN '0-2 yrs'
                WHEN Developer_Experience < 5 THEN '2-5 yrs'
                WHEN Developer_Experience < 10 THEN '5-10 yrs'
                ELSE '10+ yrs'
            END AS Experience_Bucket,
            AVG(Critical_Bugs) AS Avg_Critical_Bugs,
            AVG(Security_Vulnerabilities) AS Avg_Security_Vulns
        FROM releases
        GROUP BY Experience_Bucket
    """
    exp_df = pd.read_sql_query(exp_sql, conn)
    exp_df = exp_df.melt(id_vars="Experience_Bucket", var_name="Metric", value_name="Average")
    fig5 = px.line(
        exp_df, x="Experience_Bucket", y="Average", color="Metric", markers=True,
        category_orders={"Experience_Bucket": ["0-2 yrs", "2-5 yrs", "5-10 yrs", "10+ yrs"]}
    )
    st.plotly_chart(style_fig(fig5), use_container_width=True)

    # ---- 6. Run-your-own-query box ----
    st.markdown("---")
    st.subheader("Run Your Own SQL Query")
    st.caption("Query the `releases` table directly (SELECT statements only).")

    default_query = "SELECT Risk_Level, COUNT(*) AS Count FROM releases GROUP BY Risk_Level;"
    user_query = st.text_area("SQL Query", value=default_query, height=100)

    if st.button("Run Query"):
        try:
            if not user_query.strip().lower().startswith("select"):
                st.error("Only SELECT queries are allowed.")
            else:
                result_df = pd.read_sql_query(user_query, conn)
                st.dataframe(result_df, use_container_width=True)
        except Exception as e:
            st.error(f"Query failed: {e}")

    conn.close()
